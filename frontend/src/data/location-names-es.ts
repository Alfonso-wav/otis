/**
 * Get Spanish fallback name for a location slug.
 * Returns undefined if no translation is available.
 */
export function getLocationNameEs(slug: string): string | undefined {
  if (names[slug]) return names[slug];

  // Pattern: {region}-route-{n}
  const route = slug.match(/^[\w]+-route-(\d+)$/);
  if (route) return `Ruta ${route[1]}`;

  // Pattern: {region}-sea-route-{n}
  const sea = slug.match(/^[\w]+-sea-route-(\d+)$/);
  if (sea) return `Ruta Marina ${sea[1]}`;

  return undefined;
}
